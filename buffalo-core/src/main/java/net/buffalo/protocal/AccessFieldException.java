package net.buffalo.protocal;

public class AccessFieldException extends ProtocolException {

	private static final long serialVersionUID = -4727126214195136992L;

	public AccessFieldException() {
		super();
	}

	public AccessFieldException(String message, Throwable cause) {
		super(message, cause);
	}

	public AccessFieldException(String message) {
		super(message);
	}

	public AccessFieldException(Throwable cause) {
		super(cause);
	}
	
}
